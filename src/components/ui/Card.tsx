/**
 * iOS-Style Card Component
 * Follows Apple's Human Interface Guidelines
 */

import React from 'react';
import { cn } from '../../utils/cn';

export interface CardProps extends React.HTMLAttributes<HTMLDivElement> {
  variant?: 'default' | 'elevated' | 'outlined';
  padding?: 'none' | 'sm' | 'md' | 'lg';
  children: React.ReactNode;
}

export const Card = React.forwardRef<HTMLDivElement, CardProps>(
  ({ className, variant = 'default', padding = 'md', children, ...props }, ref) => {
    return (
      <div
        ref={ref}
        className={cn(
          // Base styles
          'rounded-xl transition-all duration-200',
          'bg-[var(--ios-bg-grouped-secondary)]',

          // Variant styles
          {
            // Default - subtle shadow
            'shadow-[var(--ios-shadow-sm)]': variant === 'default',

            // Elevated - larger shadow
            'shadow-[var(--ios-shadow-md)] hover:shadow-[var(--ios-shadow-lg)] hover:-translate-y-0.5':
              variant === 'elevated',

            // Outlined - border instead of shadow
            'border border-[var(--ios-separator)] shadow-none': variant === 'outlined',
          },

          // Padding
          {
            'p-0': padding === 'none',
            'p-3': padding === 'sm',
            'p-4': padding === 'md',
            'p-6': padding === 'lg',
          },

          className
        )}
        {...props}
      >
        {children}
      </div>
    );
  }
);

Card.displayName = 'Card';

